// Mapa nombre -> unidad, para mostrar "kg"/"un" en el stepper y en el mensaje.
const UNIT_BY_NAME = Object.fromEntries(
  PRODUCT_GROUPS.flatMap((g) => g.items.map((i) => [i.name, i.unit]))
);

// Unidad por defecto (producto "Otro" escrito a mano, o nombre desconocido).
export const DEFAULT_UNIT = "un";

/** Devuelve la unidad de venta ("kg" | "un") de un producto del catalogo. */
export function getUnit(name) {
  return UNIT_BY_NAME[name] || DEFAULT_UNIT;
}

